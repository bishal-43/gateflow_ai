async def test_cannot_approve_already_approved(client: AsyncClient, db: AsyncSession):
    """Approving an already-approved request must return 400."""
    guard_headers = await auth_headers(client, db, "GUARD")
    org_headers   = await auth_headers(client, db, "ORGANIZER")
    space = await create_space(client, org_headers)
    await link_guard_to_space(client, db, guard_headers, space)

    walkin = await client.post("/walkins/request", data={
        "space_id":     space["id"],
        "visitor_name": "Double Approve",
    }, headers=guard_headers)
    walkin_id = walkin.json()["id"]

    await client.post(f"/walkins/approve/{walkin_id}", headers=org_headers)
    resp = await client.post(f"/walkins/approve/{walkin_id}", headers=org_headers)
    assert resp.status_code == 400
